#matriz no python funciona como um conjunto de listas/vetores
#pense como um armario com varias gavetas

#definindo uma matriz 3x3
matriz = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

print(matriz)

#acessando uma linha inteira
print("Segunda linha da matriz: ", matriz[1])

#acessando elementos especificos
print("Elemento da terceria linha e primeira colula: ", matriz[2][0])

#acessando o elemento na 2º linha e 3ª coluna
elemento = matriz[1][2]
print(elemento)