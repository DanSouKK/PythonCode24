#inicia uma lista dessa forma
#lista[40, 58, 62, 30] e ordene em forma crescente

lista_num1 = [40, 58, 62, 30]
print(f"Os numeros são: {lista_num1}")

lista_num1.sort(reverse = False)
print(f"\nOs numeros em ordem crescente: {lista_num1}")

#inicio outra lista com os valores, 60, 73, 28, 11
#e ordene em ordem decrescente

lista2 = [60, 73, 28, 11]
print(f"\nOs numeros são: {lista2}")

lista2.sort(reverse = True)
